package shapes;

/**
 * Represents a geometric shape with methods to compute its circumference and area.
 */
public interface Shape {
    /**
     * Returns the circumference (perimeter) of this shape.
     * 
     * @return the circumference of the shape
     */
    double getCircumference();

    /**
     * Returns the area of this shape.
     * 
     * @return the area of the shape
     */
    double getArea();
} 
